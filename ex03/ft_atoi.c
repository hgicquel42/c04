int	ft_atoi(char *str)
{
	int		i;
	char	c;
	int		n;
	int		s;

	while(1)
	{
		c = str[i];
		if (c == '-')
			s = -1;
		if (c == '+')
			s = 1;
		if (c < '0' || c > '9')
			break ;
	}
}